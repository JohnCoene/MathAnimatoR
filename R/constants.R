#' One unit step in the positive Y direction.
#'
#' @export
UP <- function(){
    c(0, 1, 0)
}

#' One unit step in the positive X direction.
#'
#' @export
RIGHT <- function(){
    c(1, 0, 0)
}


#' One unit step in the positive Y direction.
#'
#' @export
DOWN <- function(){
    c(0, -1,  0)
}

#' One unit step in the negative X direction.
#'
#' @export
LEFT <- function(){
    c(-1, 0, 0)
}


